<script>
	
	import Lines from '../../../components/Lines.vue';
	
	import * as types from '../../../store/types.js';
	import { mapActions } from 'vuex';


	export function heroContentAnimation(trigger) {

	const hctl = new TimelineMax();

	let heroLines = document.querySelectorAll('.page-wrapper.intro .lines-wrapper span'),
	heroTextMain = document.querySelector('.intro__title_main span'),
	heroText = document.querySelector('.intro__title > div'),
	heroBtnWrapper = document.querySelector('.page-wrapper.intro .btn-wrapper .inner'),
	scrollDownLine = document.querySelector('.scroll-down'),
	letterWrapper = document.querySelector('.letter__wrapper');


	if(trigger) {
			// Amen logo
			document.querySelector('.logo__container_inner').classList.add('show');

			setTimeout(() => {
				hctl
				.set([heroLines, scrollDownLine, heroTextMain, heroText, letterWrapper], {visibility: "visible"})
				.staggerTo(heroLines, 1, {y:"0%", ease: Power4.easeOut}, 0.15)
				// .from(letterWrapper, 2, {opacity: 0, ease: Power4.easeOut}, '-=1.5')
				.from(heroTextMain, 1, {y: "100%", opacity: 0, ease: Power4.easenone}, '-=1.5')
				.from(heroText, 1, {y: "100%", opacity: 0, ease: Power4.easenone}, '-=1.5')
				.to(heroBtnWrapper, 1, {y:"0%", ease: Power4.easenone}, '-=1.5')
				.from(scrollDownLine, 2, {opacity: 0, ease: Power4.easeOut}, '-=1.5');
			}, 500);
		}
		else {
			
			// Amen logo
			document.querySelector('.intro').classList.add('no-animation');

			hctl
			.set(heroLines, {visibility: "visible", y:"0%"})
			.set(heroBtnWrapper, {visibility: "visible", y:"0%"})
			.set(scrollDownLine, {visibility: "visible"});
		}
		
	}


	export default {

		data () {
			return {

			}
		},

		methods: {
			...mapActions({
				'openModal': types.TOGGLE_MODAL_ACTION
			}),
		},

		components: {
			'lines': Lines
		},

		mounted() {

			const slogans = [
				'to provide effective digital presence',
				'to create impressive motion content',
				'to make wow-worthy things',
				'to create unmatched sites'
			]

			const rotateTl = new TimelineMax();

			let dynamicWidth, fullWidth;

			const rotator = document.querySelector('.rotator');
			const staticText = document.querySelector('.static');
			const staticWidth = staticText.offsetWidth + 5;
			const dynamicWrapper = document.querySelector('.dynamic');
			const dynamic = document.querySelector('.dynamic span');
			dynamic.textContent = slogans[0];
			dynamicWidth = dynamic.offsetWidth;
			fullWidth = staticWidth + dynamicWidth;
			rotator.style.width = fullWidth + 'px';

			let count = 0;

			function changeDynamicText() {
				count++;
				if(count > slogans.length - 1) {
					count = 0;
				}
				dynamic.textContent = slogans[count];

				setTimeout(() => {
					dynamicWidth = dynamic.offsetWidth;
					fullWidth = staticWidth + dynamicWidth;
					rotator.style.width = fullWidth + 'px';
				}, 0);
			}

			function dynamicTextRotation() {
				rotateTl.to(dynamic, 0.5, {y: "-50%", opacity: 0, ease: Power0.easeNone, onComplete: () => {
					changeDynamicText();
					setTimeout(() => {
						rotateTl.fromTo(dynamic, 0.5, {y: "50%", opacity: 0}, {y: "0%", opacity: 1, ease: Power0.easeNone});
					}, 500);
				}});
			}

			setInterval(function() {
				dynamicTextRotation();
			}, 5000);

			

			document.querySelector('.scroll-down').addEventListener('click', () => {
				fullpage_api.moveTo(2);
			});


			// parallax
			function parallaxIt(e, target, movement){
				var $this = document.querySelector('.page-wrapper.intro');
				var relX = e.pageX - $this.offsetLeft;
				var relY = e.pageY - $this.offsetTop;

				TweenMax.to(target, 4, {
					x: -1*(relX - $this.offsetWidth/2) / $this.offsetWidth * movement,
					y: -1*(relY - $this.offsetHeight/2) / $this.offsetHeight * movement,
					ease: Power4.easeOut
				})
			}

			/*document.querySelector('.page-wrapper.intro').addEventListener('mousemove', (e) => {
				var $this = document.querySelector('.page-wrapper.intro');
				var relX = e.pageX - $this.offsetLeft;
				var relY = e.pageY - $this.offsetTop;
			});*/

			// parallaxIt();

			function callParallax(e){
				parallaxIt(e, '.gray .front', -100);
				parallaxIt(e, '.gray .middle', -70);
				parallaxIt(e, '.gray .back', -50);
			}

			document.querySelector('.page-wrapper.intro').addEventListener('mousemove', (e) => {
				setTimeout(callParallax.bind(null, e), 20);
			});



		}
	}
